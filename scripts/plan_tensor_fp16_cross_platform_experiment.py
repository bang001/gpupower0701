#!/usr/bin/env python3
"""Generate a resumable cross-platform FP16 Tensor attribution package."""

from __future__ import annotations

import argparse
import shlex
from dataclasses import dataclass
from datetime import date
from pathlib import Path


@dataclass(frozen=True)
class Platform:
    active_sm: int
    blocks_per_sm: tuple[int, ...]
    binary: str


PLATFORMS = {
    "rtx3090": Platform(82, (4, 8, 16), "./build/a100_fp16_energy_v2"),
    "v100": Platform(80, (4, 16, 32), "./build-v100/a100_fp16_energy_v2"),
    "a100": Platform(108, (4, 16, 32), "./build-a100/a100_fp16_energy_v2"),
    "h100": Platform(132, (4, 16, 32), "./build-h100/a100_fp16_energy_v2"),
}


PRESETS = {
    "pilot": {
        "reuse_factors": (1, 4, 16),
        "durations": (5, 15),
        "repeats": 1,
        "status": "diagnostic_only",
    },
    "final": {
        "reuse_factors": (1, 2, 4, 8, 16),
        "durations": (5, 15, 30),
        "repeats": 3,
        "status": "strict_candidate",
    },
}


def csv_values(values: tuple[int, ...]) -> str:
    return ",".join(str(value) for value in values)


def shell_quote(value: str) -> str:
    return shlex.quote(value)


def build_script(
    *,
    profile_name: str,
    gpu_id: int,
    active_sm: int,
    binary: str,
    tag: str,
    preset_name: str,
) -> str:
    platform = PLATFORMS[profile_name]
    preset = PRESETS[preset_name]
    blocks = csv_values(platform.blocks_per_sm)
    reuse_factors = csv_values(preset["reuse_factors"])
    durations = csv_values(preset["durations"])
    repeats = int(preset["repeats"])
    prefix = f"{profile_name}_tensor_fp16_cross_platform_{preset_name}_{tag}"
    raw = f"results/raw/{prefix}.csv"
    manifest = f"results/summary/{prefix}_manifest.csv"
    calibration = f"results/raw/{prefix}_calibration.csv"
    design = f"results/summary/{prefix}_design.csv"
    ncu_dir = f"results/ncu/{prefix}_ncu"
    ncu_raw = f"results/raw/{prefix}_ncu.csv"
    ncu_acceptance = f"results/summary/{prefix}_ncu_acceptance.csv"
    ncu_acceptance_md = f"results/summary/{prefix}_ncu_acceptance.md"
    stem = f"results/summary/{prefix}"
    report = f"docs/results/{prefix}_ko.md"
    figure_dir = "docs/assets/component_energy_method"
    log = f"results/summary/{prefix}_run.log"

    pair_count = (
        len(platform.blocks_per_sm)
        * len(preset["reuse_factors"])
        * len(preset["durations"])
        * repeats
    )
    energy_rows = pair_count * 4
    ncu_cases = len(platform.blocks_per_sm) * (
        1 + 2 * len(preset["reuse_factors"])
    )

    return rf"""#!/usr/bin/env bash
set -euo pipefail

# Generated by scripts/plan_tensor_fp16_cross_platform_experiment.py.
# Preset status: {preset['status']}. Pilot output cannot be promoted to final.

TARGET_PROFILE={shell_quote(profile_name)}
GPU="${{GPU:-{gpu_id}}}"
ACTIVE_SM="${{ACTIVE_SM:-{active_sm}}}"
BIN="${{BIN:-{binary}}}"
NCU="${{NCU:-${{NCU_BIN:-ncu}}}}"
NVCC="${{NVCC:-nvcc}}"
NCU_USE_SUDO="${{NCU_USE_SUDO:-0}}"
NCU_AUTO_SUDO="${{NCU_AUTO_SUDO:-1}}"
FORCE_NCU="${{FORCE_NCU:-0}}"
PAIR_COOLDOWN_MAX_POWER_W="${{PAIR_COOLDOWN_MAX_POWER_W:-1000000}}"
IDLE_READY_MAX_POWER_W="${{IDLE_READY_MAX_POWER_W:-0}}"

RAW={shell_quote(raw)}
MANIFEST={shell_quote(manifest)}
CALIBRATION={shell_quote(calibration)}
DESIGN={shell_quote(design)}
NCU_DIR={shell_quote(ncu_dir)}
NCU_RAW={shell_quote(ncu_raw)}
NCU_ACCEPTANCE={shell_quote(ncu_acceptance)}
NCU_ACCEPTANCE_MD={shell_quote(ncu_acceptance_md)}
LOG={shell_quote(log)}

mkdir -p results/raw results/summary results/ncu docs/results {figure_dir}
exec > >(tee -a "${{LOG}}") 2>&1

echo "profile=${{TARGET_PROFILE}} gpu=${{GPU}} active_SM=${{ACTIVE_SM}}"
echo "B={blocks} RF={reuse_factors} duration={durations}s repeats={repeats}"
echo "planned_pairs={pair_count} planned_energy_rows={energy_rows}"
echo "planned_tensor_ncu_cases={ncu_cases}"

if [[ ! -x "${{BIN}}" ]]; then
  echo "binary is missing or not executable: ${{BIN}}" >&2
  exit 2
fi

python3 scripts/preflight_gpu_support.py \
  --gpu "${{GPU}}" \
  --target-profile "${{TARGET_PROFILE}}" \
  --binary "${{BIN}}" \
  --active-sm "${{ACTIVE_SM}}" \
  --ncu "${{NCU}}" \
  --nvcc "${{NVCC}}" \
  --strict \
  --out {shell_quote(stem + '_preflight.md')}

python3 scripts/run_component_dynamic_attribution.py \
  --target-profile "${{TARGET_PROFILE}}" \
  --binary "${{BIN}}" \
  --gpu-id "${{GPU}}" \
  --active-sm "${{ACTIVE_SM}}" \
  --blocks-per-sm-values {shell_quote(blocks)} \
  --components tensor \
  --reuse-factors {shell_quote(reuse_factors)} \
  --load-repeats 4 \
  --durations {shell_quote(durations)} \
  --repeats {repeats} \
  --calibration-policy factorial_grid \
  --baseline-seconds 3 \
  --control-min-seconds 0.25 \
  --idle-settle-seconds 5 \
  --idle-measure-seconds 10 \
  --idle-ready-max-power-w "${{IDLE_READY_MAX_POWER_W}}" \
  --pair-cooldown \
  --cooldown-max-temperature-c 65 \
  --cooldown-max-power-w "${{PAIR_COOLDOWN_MAX_POWER_W}}" \
  --cooldown-max-gpu-util-pct 1 \
  --cooldown-max-memory-util-pct 5 \
  --cooldown-consecutive-samples 3 \
  --max-cooldown-seconds 180 \
  --max-command-wall-seconds 300 \
  --max-predicted-treatment-seconds 90 \
  --pre-energy-cooldown-seconds 10 \
  --output "${{RAW}}" \
  --manifest "${{MANIFEST}}" \
  --calibration-csv "${{CALIBRATION}}" \
  --design-csv "${{DESIGN}}" \
  --resume \
  --execute

NCU_ACCEPTANCE_REUSABLE=0
if [[ -s "${{NCU_ACCEPTANCE}}" ]] && python3 -c '
import csv, hashlib, sys
acceptance_path, binary_path, expected_text, blocks_text, factors_text = sys.argv[1:]
with open(binary_path, "rb") as binary_handle:
    digest = hashlib.sha256(binary_handle.read()).hexdigest()
with open(acceptance_path, newline="", encoding="utf-8") as handle:
    rows = list(csv.DictReader(handle))
hashes = {{row.get("ncu_binary_sha256", "") for row in rows}}
captures = {{row.get("ncu_binary_hash_capture", "") for row in rows}}
blocks = [int(value) for value in blocks_text.split(",")]
factors = [int(value) for value in factors_text.split(",")]
expected_labels = set()
for block in blocks:
    expected_labels.add(f"clocked_empty_W64_B{{block}}")
    for factor in factors:
        expected_labels.add(f"reg_operand_only_W1_B{{block}}_RF{{factor}}")
        expected_labels.add(f"reg_mma_W1_B{{block}}_RF{{factor}}")
labels = {{row.get("label", "") for row in rows}}
ok = len(rows) == int(expected_text) and labels == expected_labels and hashes == {{digest}} and captures == {{"pre_post_collection_verified"}}
raise SystemExit(0 if ok else 1)
' "${{NCU_ACCEPTANCE}}" "${{BIN}}" {ncu_cases} {shell_quote(blocks)} {shell_quote(reuse_factors)}; then
  NCU_ACCEPTANCE_REUSABLE=1
fi

if [[ "${{FORCE_NCU}}" == "1" || "${{NCU_ACCEPTANCE_REUSABLE}}" != "1" ]]; then
  TARGET_PROFILE="${{TARGET_PROFILE}}" \
  GPU="${{GPU}}" \
  ACTIVE_SM="${{ACTIVE_SM}}" \
  BIN="${{BIN}}" \
  NCU="${{NCU}}" \
  NCU_USE_SUDO="${{NCU_USE_SUDO}}" \
  NCU_AUTO_SUDO="${{NCU_AUTO_SUDO}}" \
  ATTRIBUTION_COMPONENTS=tensor \
  BLOCKS_PER_SM_VALUES={shell_quote(blocks)} \
  TENSOR_REUSE_FACTORS={shell_quote(reuse_factors)} \
  OUTDIR="${{NCU_DIR}}" \
  RAW_OUT="${{NCU_RAW}}" \
  ACCEPTANCE_CSV="${{NCU_ACCEPTANCE}}" \
  ACCEPTANCE_MD="${{NCU_ACCEPTANCE_MD}}" \
  bash scripts/run_component_dynamic_attribution_ncu.sh
else
  echo "NCU acceptance is complete and matches the current binary hash; set FORCE_NCU=1 to recollect it"
fi

python3 scripts/analyze_component_dynamic_attribution.py \
  --manifest "${{MANIFEST}}" \
  --ncu-acceptance "${{NCU_ACCEPTANCE}}" \
  --out-detail {shell_quote(stem + '_detail.csv')} \
  --out-summary {shell_quote(stem + '_summary.csv')} \
  --out-subgroups {shell_quote(stem + '_by_blocks.csv')} \
  --out-regression {shell_quote(stem + '_regression.csv')} \
  --out-ncu-evidence {shell_quote(stem + '_ncu_evidence.csv')} \
  --out-ncu-by-blocks {shell_quote(stem + '_ncu_by_blocks.csv')} \
  --out-tensor-rf-trends {shell_quote(stem + '_tensor_rf_trends.csv')} \
  --out-report {shell_quote(report)} \
  --figure-prefix {shell_quote(prefix)} \
  --figure-relative-dir ../assets/component_energy_method

python3 scripts/plot_component_dynamic_attribution.py \
  --detail {shell_quote(stem + '_detail.csv')} \
  --summary {shell_quote(stem + '_summary.csv')} \
  --regression {shell_quote(stem + '_regression.csv')} \
  --ncu-evidence {shell_quote(stem + '_ncu_evidence.csv')} \
  --output-dir {shell_quote(figure_dir)} \
  --prefix {shell_quote(prefix)}

echo "report={report}"
echo "tensor_rf_trends={stem}_tensor_rf_trends.csv"
"""


def self_test() -> None:
    script = build_script(
        profile_name="a100",
        gpu_id=1,
        active_sm=108,
        binary=PLATFORMS["a100"].binary,
        tag="selftest",
        preset_name="final",
    )
    assert "B=4,16,32 RF=1,2,4,8,16 duration=5,15,30s repeats=3" in script
    assert "ATTRIBUTION_COMPONENTS=tensor" in script
    assert "--resume" in script
    assert "--pair-cooldown" in script
    assert "planned_pairs=135" in script
    assert "planned_energy_rows=540" in script
    assert "planned_tensor_ncu_cases=33" in script
    assert "NCU_ACCEPTANCE_REUSABLE" in script
    assert "--out-tensor-rf-trends" in script
    assert "preflight_gpu_support.py" in script
    assert "--figure-prefix" in script
    print("cross-platform FP16 Tensor planner self-test passed")


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--self-test", action="store_true")
    parser.add_argument("--target-profile", choices=sorted(PLATFORMS))
    parser.add_argument("--gpu-id", type=int, default=0)
    parser.add_argument("--active-sm", type=int, default=0)
    parser.add_argument("--binary", default="")
    parser.add_argument("--preset", choices=sorted(PRESETS), default="final")
    parser.add_argument("--tag", default=date.today().strftime("%Y%m%d"))
    parser.add_argument("--output-script", default="")
    parser.add_argument("--print-only", action="store_true")
    args = parser.parse_args()
    if args.self_test:
        self_test()
        return 0
    if not args.target_profile:
        parser.error("--target-profile is required")
    if args.gpu_id < 0:
        parser.error("--gpu-id must be non-negative")
    platform = PLATFORMS[args.target_profile]
    active_sm = args.active_sm or platform.active_sm
    if active_sm <= 0:
        parser.error("--active-sm must be positive")
    binary = args.binary or platform.binary
    script = build_script(
        profile_name=args.target_profile,
        gpu_id=args.gpu_id,
        active_sm=active_sm,
        binary=binary,
        tag=args.tag,
        preset_name=args.preset,
    )
    if args.print_only:
        print(script, end="")
        return 0
    output = Path(
        args.output_script
        or f"results/summary/{args.target_profile}_tensor_fp16_cross_platform_"
        f"{args.preset}_{args.tag}_command.sh"
    )
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(script, encoding="utf-8")
    output.chmod(0o755)
    print(f"wrote command package: {output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
